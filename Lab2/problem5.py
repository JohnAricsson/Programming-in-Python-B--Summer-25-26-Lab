marks = int(input("Enter grade number: "))

if marks >= 90:
    print("Grade: A+")
elif marks >= 85:
    print("Grade: A")
elif marks >= 80:
    print("Grade: B+")
elif marks >= 75:
    print("Grade: B")
elif marks >= 70:
    print("Grade: B-")
elif marks >= 65:
    print("Grade: C+")
elif marks >= 60:
    print("Grade: C")
elif marks >= 55:
    print("Grade: C-")
elif marks >= 50:
    print("Grade: D")
else:
    print("Grade: F")